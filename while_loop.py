# a while loop is a structure in python that allows us to loop through and execute a block of code repeatedly until a certian condition is false.

# as long as the condition provided to the loop remains true, the loop continues to run in circles.
# it goes back to check the condition after each iteration

i = 1

while i <= 10:
  print(i)
  i += 1
  
  
print("done looping")  
  